public class switch_statement {
    public static void main(String[] args) {
        int a = 10;
        switch (a) {
            case 10:
                System.out.println("a is 10");
                break;
            case 20:
                System.out.println("a is 20");
                break;
            case 30:
                System.out.println("a is 30");
                break;
        }
    }
}
