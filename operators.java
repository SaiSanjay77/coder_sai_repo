public class operators{
    public static void main(String[] args) {
        int a=10 ;
        int b=20 ;
        System.out.println("The sum of a and b is:");
        System.out.println(a+b);
        System.out.println("The product of a and b is:");
        System.out.println(a*b);
        System.out.println("The difference of a and b is:");
        System.out.println(a-b);
        System.out.println("The quotient of a and b is:");
        System.out.println(a%b);
        // getting fractional part
        int c = 30;
        int d = 9;
        System.out.println("The fractional part of c/d is:");
        double e = (double)c/d;
        System.out.println(e);
    }
}
